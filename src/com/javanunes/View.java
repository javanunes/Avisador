    private boolean temEspacosHoras(){
        if( txtHora1.getText().contains(" ") ){
            JOptionPane.showMessageDialog (null, "Primeiro horário contem espaço!");
            txtHora1.grabFocus();
            return true;
        }
        
        if( txtHora2.getText().contains(" ") ){
            JOptionPane.showMessageDialog (null, "Segundo horário contem espaço!");
            txtHora2.grabFocus();
            return true;
        }
        
        if( txtHora3.getText().contains(" ") ){
            JOptionPane.showMessageDialog (null, "Terceiro horário contem espaço!");
            txtHora3.grabFocus();
            return true;
        }
        
        if( txtHora4.getText().contains(" ") ){
            JOptionPane.showMessageDialog (null, "Quarto horário contem espaço!");
            txtHora4.grabFocus();
            return true;
        }
        return false;
    }

