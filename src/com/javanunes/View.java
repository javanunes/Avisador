    private void btComecarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btComecarActionPerformed
        // TODO add your handling code here:
        
        // Se as horas que o usuario colocou não tem espaços
        if(!temEspacosHoras()){
            
             // Chama a thread t para ficar conferindo quando o horário é igual ao que usuario inseriu
             Clock hora = new Clock();
             Thread t = new Thread(hora);
             t.start();

             btComecar.setEnabled(false);
             txtHora1.setEditable(false);
             txtHora2.setEditable(false);
             txtHora3.setEditable(false);
             txtHora4.setEditable(false);

             // Salva as horas inseridas pelo usuario em um arquivo
             Arquivo.setHoras(txtHora1.getText(), txtHora2.getText(), txtHora3.getText(), txtHora4.getText());
        }
    }//GEN-LAST:event_btComecarActionPerformed

